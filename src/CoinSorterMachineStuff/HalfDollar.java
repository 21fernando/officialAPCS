package CoinSorterMachineStuff;
/**
 * Class that represents a Half Dollar and extends the abstract Coin class
 */
public class HalfDollar extends Coin {
    /**
     * Gets the value of a Half Dollar
     * @return value of a Half Dollar
     */
    public double getValue(){
        return 0.5;
    }
    /**
     * Gets name of a Half Dollar
     * @return the name of a Half Dollar
     */
    public String getName(){
        return "Half Dollar";
    }

}
