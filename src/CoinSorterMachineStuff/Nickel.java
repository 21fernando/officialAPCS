package CoinSorterMachineStuff;
/**
 * Class that represents a Nickel and extends the abstract Coin class
 */
public class Nickel extends Coin {
    /**
     * Gets the value of a Nickel
     * @return value of a Nickel
     */
    public double getValue(){
        return 0.05;
    }
    /**
     * Gets name of a Nickel
     * @return the name of a Nickel
     */
    public String getName(){
        return "Nickel";
    }

}
