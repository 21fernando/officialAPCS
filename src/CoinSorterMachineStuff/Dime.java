package CoinSorterMachineStuff;

/**
 * Class that represents a Dime and extends the abstract Coin class
 */
public class Dime extends Coin {
    /**
     * Gets the value of a dime
     * @return value of a dime
     */
    public double getValue(){
        return 0.1;
    }

    /**
     * Gets name of a Dime
     * @return the name of a dime
     */
    public String getName(){
        return "Dime";
    }

}
