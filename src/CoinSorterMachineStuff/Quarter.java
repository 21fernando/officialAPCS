package CoinSorterMachineStuff;
/**
 * Class that represents a Quarter and extends the abstract Coin class
 */
public class Quarter extends Coin{
    /**
     * Gets the value of a Quarter
     * @return value of a Quarter
     */
    public double getValue(){
        return 0.25;
    }
    /**
     * Gets name of a Quarter
     * @return the name of a Quarter
     */
    public String getName(){
        return "Quarter";
    }

}
